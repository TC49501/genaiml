from transformers import pipeline

# Load the summarizer pipeline with a pretrained model
summarizer = pipeline("summarization", model="google/pegasus-xsum")

def summarize_text(text):
    result = summarizer(text, max_length=130, min_length=30, do_sample=False)
    return result[0]['summary_text']

from IPython.display import display
import ipywidgets as widgets

text_area = widgets.Textarea(
    value='Paste a long text here...',
    placeholder='Enter text to summarize',
    description='Input:',
    layout=widgets.Layout(width='100%', height='200px')
)

button = widgets.Button(description="Summarize")
output = widgets.Output()

def on_button_click(b):
    with output:
        output.clear_output()
        summary = summarize_text(text_area.value)
        print("üìù Summary:\n", summary)

button.on_click(on_button_click)

display(text_area, button, output)
